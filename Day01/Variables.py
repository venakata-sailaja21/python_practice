# ============================
# 1. Integer Variable
# ============================
age = 22
print("Age:", age)

# ============================
# 2. String Variable
# ============================
name = "Sailu"
print("Name:", name)

# ============================
# 3. Float Variable
# ============================
height = 5.6
print("Height:", height)

# ============================
# 4. Boolean Variable
# ============================
is_student = True
print("Student:", is_student)

# ============================
# 5. Multiple Variable Assignment
# ============================
x, y, z = 10, 20, 30
print(x, y, z)

# ============================
# 6. Assign Same Value
# ============================
a = b = c = 100
print(a, b, c)

# ============================
# 7. Variable Swapping
# ============================
num1 = 10
num2 = 20
num1, num2 = num2, num1
print("num1 =", num1)
print("num2 =", num2)
